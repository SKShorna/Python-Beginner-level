
name = "Sabira"
age = 32
height = 1.6

# using type casting
print("My name is " + name + "." + "I am " + str(age) + " year's old." + " My height is " + str(height) + ".")

# using comma

print("My name is ", name, ".", "I am ", age, "year's old.", "My height is", height, ".")

# using f string

print(f"My name is {name}. I am {age} year's old. My height is {height}.")
print(f"My father is {age*2} year's old.")